object ExploringOperators {
    //Exercise #8: Further exploring operators
    def main(args: Array[String]): Unit = {
      var y: Int = 5
      var z:Int = 6
      println(y+" + "+z+ " = " +(y+z))
      println(y+" x "+z+ " = " +(y*z))
      println(y+" - "+z+ " = " +(y-z))
      println(y+"^2 + "+z+ "^2 = " +(y*y+z*z))

    }
}
